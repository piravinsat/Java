package calc;

/**
 * A list of the non-number tokens possible in an expression.
 * @author Piravin Satkunarajah
 */




public enum Symbol {

    /**
     * This represents the "(" symbol in an expression.
     */
    LEFT_BRACKET("("),
    /**
     * This represents the ")" symbol in an expression.
     */
    RIGHT_BRACKET(")"),
    /**
     * This represents the "*" symbol in a expression.
     */
    TIMES("*"),
    /**
     * This represents the "/" symbol in a expression.
     */
    DIVIDE("/"),
    /**
     * This represents the "+" symbol in a expression.
     */
    PLUS("+"),
    /**
     * This represents the "-" symbol in a expression.
     */
    MINUS("-"),
    /**
     * The represents the value that isn't the above symbols.
     */
    INVALID("?");

    /**
     * This instance holds a string value.
     */
    private String val;

    /**
     * The private constructor for the Symbol class.
     * @param what String value
     */
    private Symbol(final String what) {
        val = what;
    }
    /**
     * toString method.
     * @return the String version of val.
     */
    public String toString() {
        return new String(val);
    }
}
