package agents;
/**
 * AgentType represents the two possible classifications of an Agent.
 * <p>
 * @author Piravin Satkunarajah
 * @author Luigi Pardey
 */
public enum AgentType {
    /**
     * Represents a customer that buys items at the Supermarket.
     */
    CUSTOMER,
    /**
     * Represents a member of staff who works at the Supermarket serving
     * the Customers.
     */
    STAFF
}
